import React from "react";
import styles from './Nav.module.scss'
import {NavLink, Link} from 'react-router-dom'
import { useLocation } from "react-router";

import { FontAwesomeIcon } from '@fortawesome/react-fontawesome'
import { faFilter, faPlus} from '@fortawesome/free-solid-svg-icons'
import usersStore from "../../zustand/usersStore";
import Button from "../Button/Button";
const Nav = ({setIsModalOpen}) => {
    const location = useLocation()
    const user = usersStore((state) => state.user)
    return(
        <div className={styles.wrapper}>
            <div className={styles.nav}>
                <div className={styles.left}>
                    <NavLink to="/tasks">{({ isActive }) => (<Button  secondary={isActive} text="Zadania"/>)}</NavLink>
                    <NavLink to="/statistics">{({ isActive }) => (<Button style={{marginLeft: "2rem"}} secondary={isActive} text="Statystyki"/>)}</NavLink>
                    {user?.isAdmin && <NavLink to="/users">{({ isActive }) => (<Button style={{marginLeft: "2rem"}} secondary={isActive} text="UÅ¼ytkownicy"/>)}</NavLink>}
                </div>
                <div className={styles.right}>
                    <div className={styles.button} onClick={() => setIsModalOpen(true)}>
                        {location.pathname.includes("/users") ?  
                            <Link to="/users"><p className={styles.buttonContent}>
                                Dodaj <FontAwesomeIcon icon={faPlus}/></p
                            ></Link>  
                            : 
                            <Link to={location.pathname.includes('/tasks') ? "/tasks" : "/statistics"}>
                                <p className={styles.buttonContent}>Filtry <FontAwesomeIcon icon={faFilter}/></p>
                            </Link>}
                    </div>
                </div>
            </div>
            <div className={styles.line}></div>
        </div>
    )
}
export default Nav